device = 'cpu'
def process_data(batch,args):
    text1 = torch.LongTensor(batch['text1']).to(device)
    text2 = torch.LongTensor(batch['text2']).to(device)
    mask1 = torch.ne(text1, args.padding).unsqueeze(2)
    mask2 = torch.ne(text2, args.padding).unsqueeze(2)
    inputs = {
        'text1': text1,
        'text2': text2,
        'mask1': mask1,
        'mask2': mask2,
    }
    if 'target' in batch:
        target = torch.LongTensor(batch['target']).to(device)
        return inputs, target
    return inputs, None
def predict(model,batch,args):
    model.eval()
    inputs, _ = process_data(batch,args)
    with torch.no_grad():
        output = model(inputs)
        output = torch.nn.functional.softmax(output, dim=1)
    return output.tolist()

def load_model(model_path):
    checkpoint = torch.load(model_path)
    args = checkpoint['args']
    model = Model(args,checkpoint['state_dict']).network
    return [model,args]
